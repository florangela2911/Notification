    /*
    |--------------------------------------------------------------------------
    |  Add Column
    |--------------------------------------------------------------------------
    | Make Datasource fields available to be used as columns.
    | You can pass a closure to transform/modify the data.
    |
    | ❗ IMPORTANT: When using closures, you must escape any value coming from
    |    the database using the `e()` Laravel Helper function.
    |
    */
    public function addColumns(): PowerGridColumns
    {
        return PowerGrid::columns()
            ->addColumn('id')
            ->addColumn('name')

           /** Example of custom column using a closure **/
            ->addColumn('name_lower', fn (usuarios_gs $model) => strtolower(e($model->name)))

            ->addColumn('email')
            ->addColumn('created_at_formatted', fn (usuarios_gs $model) => Carbon::parse($model->created_at)->format('d/m/Y H:i:s'));
    }

    /*
    |--------------------------------------------------------------------------
    |  Include Columns
    |--------------------------------------------------------------------------
    | Include the columns added columns, making them visible on the Table.
    | Each column can be configured with properties, filters, actions...
    |
    */

     /**
      * PowerGrid Columns.
      *
      * @return array<int, Column>
      */
    public function columns(): array
    {
        return [
            Column::make('Id', 'id'),
            Column::make('nombre', 'historia')
                ->sortable()
                ->searchable(),

            Column::make('fecha_cita', 'fechacita')
                ->sortable()
                ->searchable(),

            Column::make('hora', 'hora')
                ->sortable()
                ->searchable(),

            Column::make('profesional', 'nombre')
                ->sortable()
                ->searchable(),

            Column::make('servicio', 'procedim')
                ->sortable()
                ->searchable(),

            Column::make('sede', 'sede')
                ->sortable()
                ->searchable(),

            Column::make('direccion', 'direccion')
                ->sortable()
                ->searchable(),

            Column::make('gmail', 'gmail')
                ->sortable()
                ->searchable(),

            Column::make('Created at', 'created_at_formatted', 'created_at')
                ->sortable(),

        ];
    }

    /**
     * PowerGrid Filters.
     *
     * @return array<int, Filter>
     */
    public function filters(): array
    {
        return [
            Filter::inputText('historia')->operators(['contains']),
            Filter::inputText('fechacita')->operators(['contains']),
            Filter::inputText('hora')->operators(['contains']),
            Filter::inputText('nombre')->operators(['contains']),
            Filter::inputText('procedim')->operators(['contains']),
            Filter::inputText('sede')->operators(['contains']),
            Filter::inputText('direccion')->operators(['contains']),
            Filter::inputText('gmail')->operators(['contains']),
            Filter::datetimepicker('created_at'),
        ];
    }

